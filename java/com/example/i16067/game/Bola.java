package com.example.i16067.game;

public class Bola extends Lingkaran {

    protected float x,y;
    protected int r;

    public Bola(float x, float y,int r)  {
        super(x,y,r);
    }

    public float getX() {
        return super.getX();
    }

    public float getY() {
        return super.getY();
    }

    public int getR() {
        return super.getR();
    }

    public void setX(float x) {
        super.setX(x);
    }

    public void setY(float y) {
        super.setY(y);
    }


}